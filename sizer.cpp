#include <iostream>

int main()
{

    std::cout << "The size of an int is " << sizeof(int) << "\n";
    std::cout << "The size of a long is " << sizeof(long) << "\n";
    std::cout << "The size of a short is " << sizeof(short) << "\n";
    std::cout << "The size of a long long is " << sizeof(long long) << "\n";
    std::cout << "The size of a boolean is " << sizeof(bool) << "\n";

    return 0;
}